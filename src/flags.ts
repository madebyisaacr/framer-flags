// Convert ISO code to flag emoji. [web:6][web:20]
function codeToFlag(code: string) {
	return code
		.toUpperCase()
		.split("")
		.map((c) => String.fromCodePoint(0x1f1e6 + c.charCodeAt(0) - 65))
		.join("");
}

// Generate all country/territory flags + 3 RGI subdivision flags (England, Scotland, Wales). [web:1][web:19]
export function generateAllFlagEmojis() {
	const flags = VALID_COUNTRY_CODES.map(codeToFlag);

	// RGI subdivision flags (tag sequences). [web:19][page:0]
	flags.push(
		"\u{1F3F4}\u{E0067}\u{E0062}\u{E0065}\u{E006E}\u{E0067}\u{E007F}", // 🏴󠁧󠁢󠁥󠁮󠁧󠁿 England
		"\u{1F3F4}\u{E0067}\u{E0062}\u{E0073}\u{E0063}\u{E0074}\u{E007F}", // 🏴󠁧󠁢󠁳󠁣󠁴󠁿 Scotland
		"\u{1F3F4}\u{E0067}\u{E0062}\u{E0077}\u{E006C}\u{E0073}\u{E007F}" // 🏴󠁧󠁢󠁷󠁬󠁳󠁿 Wales
	);

	return flags.sort();
}
